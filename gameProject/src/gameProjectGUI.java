    /**
     * checks if the button pushed is the last one in the word
     */
    public void checkDone(){
        boolean done=true;
        for (int i=0; i<correct.length; i++){
            if (correct[i]!=true){
                done=false;
            }
        }
        if (done==true){
            gameMenu.setVisible(false);
            finalMenu.setVisible(true);
        } 
    }
